#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*ptr_s;

	ptr_s = (unsigned char *)s;
	i = 0;
	while (n--)
	{
		if (ptr_s[i] == (unsigned char)c)
			return (ptr_s);
		ptr_s++;
	}
	return (NULL);
}
